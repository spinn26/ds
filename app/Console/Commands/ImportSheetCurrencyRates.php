        $now = Carbon::now();
        $written = 0;

        foreach (self::RATES as $currency => $byYear) {
            foreach ($byYear as $year => $months) {
                foreach ($months as $idx => $rate) {
                    if ($rate === null) {
                        continue;
                    }
                    $date = sprintf('%04d-%02d-01', $year, $idx + 1);
                    DB::table('management_currency_rate')->updateOrInsert(
                        ['currency' => $currency, 'date' => $date],
                        ['rate' => $rate, 'updated_at' => $now, 'created_at' => $now],
                    );
                    $written++;
                }
            }
        }

        $this->info("Управленческие курсы загружены: {$written} строк (USD/EUR/GBP, 2021–2026).");

        return self::SUCCESS;
